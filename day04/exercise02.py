# 练习： 在终端中录入一个整数, 打印每位相加和
# 例如：“1234” -->  “1”  --> 1  --> 1 + 0
#                  “2”  --> 2  --> 2 + 1
#                  “3”  --> 3  --> 3 + 3
#                  “4”  --> 4  --> 4 + 6
#                  .....       --> 当前 + 之前累加结果 -->  之前 += 当前
value = 0
number = input("请输入整数")
for item in number:
    value += int(item)
print(value)
